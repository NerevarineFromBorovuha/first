package linear_programs;

public class Ex15 {

	public static void main(String[] args) {
		// Написать программу, которая выводит на экран первые четыре степени числа π.
		double pi1;
		double pi2;
		double pi3;
		double pi4;
		pi1 = Math.PI;
		pi2 = pi1 * pi1;
		pi3 = pi1 * pi1 * pi1;
		pi4 = pi1 * pi1 * pi1 * pi1;
		System.out.println("pi1 = " + pi1 + "\npi2 = " + pi2 + "\npi3 = " + pi3 + "\npi4 = " + pi4);

	}
}